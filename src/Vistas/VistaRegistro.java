    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        // TODO add your handling code here:
        String nombre = userField.getText();
        int id_rol = comboBoxRoles.getSelectedIndex()+1;
        String pass = passField1.getText();
        String passConf = passField2.getText();
        int id_carrera = comboBoxCarreras.getSelectedIndex()+1;
        boolean completo = conreg.obtenerDatos(nombre, id_rol, pass, passConf, id_carrera);
        if (completo){
            // por mover a controlador
            idJugadorRegistrado = conreg.obtenerUltimaID();
            
            solicitarPersonaje(this.comboBoxRoles.getSelectedItem().toString());
            panel.setVisible(true);
            panel2.setVisible(false);
            jButton3.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Registro incompleto. Porfavor, intente nuevamente", "Error", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

