    private void solicitarPersonaje(String selectedItem) {
        String[] personaje = conreg.buscarPersonaje(selectedItem).split("CORTAR");
        jLabel2.setText(personaje[0]);
        jLabel7.setText(personaje[1]);
        int vida = Integer.parseInt(personaje[4]);
        int defensa = Integer.parseInt(personaje[5]);
        int ataque = Integer.parseInt(personaje[6]);
        int movimiento = Integer.parseInt(personaje[7]);
        int magia = Integer.parseInt(personaje[8]);
        ImageIcon fotoPj = new ImageIcon(this.getClass().getResource(personaje[9]));
        labelCara.setIcon(fotoPj);
        barra1.setMaximum(vida); barra1.setValue(vida); barra1.setString(personaje[4]);
        barra2.setMaximum(defensa); barra2.setValue(defensa);barra2.setString(personaje[5]);
        barra3.setMaximum(ataque); barra3.setValue(ataque);barra3.setString(personaje[6]);
        barra4.setMaximum(movimiento); barra4.setValue(movimiento);barra4.setString(personaje[7]);
        barra5.setMaximum(magia); barra5.setValue(magia);barra5.setString(personaje[8]);
    }

    
    
